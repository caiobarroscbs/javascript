// 2. Dado o tamanho do lado de um quadrado, calcular a área e o perímetro do mesmo.

let tamQuadrado = Number (prompt('Qual o tamanho do lado do quadrado?'))

let areaQuadrado = tamQuadrado * tamQuadrado
let periQuadrado = tamQuadrado * 2 + tamQuadrado * 2

alert ('A área do quadrado é: ' + areaQuadrado)
alert ('O perimetro do quadrado é: ' + periQuadrado)